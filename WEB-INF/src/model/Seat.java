package model;

public class Seat {
	private int seat_colum;
	private char seat_row;
	public int getSeat_colum() {
		return seat_colum;
	}
	public void setSeat_colum(int seat_colum) {
		this.seat_colum = seat_colum;
	}
	public char getSeat_row() {
		return seat_row;
	}
	public void setSeat_row(char seat_row) {
		this.seat_row = seat_row;
	}

}
